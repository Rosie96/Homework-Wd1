mood = str(input("How are you today?"))

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Don`t worry, tomorrow will be better")
elif mood == "excited":
    print("Wow, it's good that you're enjoying life")
elif mood == "relaxed":
    print("Keep on chilling")
else:
    print("I am sorry, I cannot recognize this mood")